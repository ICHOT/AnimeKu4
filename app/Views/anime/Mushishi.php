         Dunia ini bukan hanya ditinggali oleh manusia, tumbuh-tumbuhan, hewan dan lain sebagainya, ada satu lagi sosok makhluk aneh yang sering dipanggil dengan nama “Mushi”.<br><br>

         Mushi merupakan makhluk yang tak memiliki bentuk dan tidak juga mengalami kematian, mungkin sekarang bisa disebut sejenih dengan roh atau semacamnya, bahkan mushi sendiri dapat berubah, berprilaku dan berpura-pura menjadi sosok makhluk lainnya. namun Mushi sendiri hanya dapat terlihat oleh beberapa orang.<br><br>

